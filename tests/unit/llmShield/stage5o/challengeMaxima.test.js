test("boundary: noncanonical JSON byte encoding REJECT", () => {
  // A canonical-size limit is sound ONLY when canonical encoding is a condition of acceptance.
  // Each of these parses to the SAME object and is byte-wise noncanonical.
  const record = maximalChallengeRecord();
  const canonical = canonicalJson(record);
  assert.doesNotThrow(() => requireCanonicalBytes(canonical));

  const whitespace = JSON.stringify(JSON.parse(canonical), null, 2);
  const reordered = JSON.stringify(
    Object.fromEntries(Object.entries(JSON.parse(canonical)).reverse())
  );

  for (const [name, raw] of Object.entries({ whitespace, reordered })) {
    assert.notEqual(raw, canonical, `${name}: PREMISE FAILED — the mutation did not change bytes`);
    assert.deepEqual(JSON.parse(raw), JSON.parse(canonical), `${name}: premise — same object`);
    assert.throws(() => requireCanonicalBytes(raw), `${name} must REJECT`);
  }

  // and the attack this rule exists to stop: expansion under an aggregate limit
  assert.ok(
    Buffer.byteLength(whitespace, "utf8") > Buffer.byteLength(canonical, "utf8"),
    "premise: whitespace expansion is strictly larger than the canonical maximum"
  );
});
