/** The negative-fixture law, carried forward from the codec vectors. */
function assertNegativeVector({ name, valid, mutated, accept, property, propertyName }) {
  assert.notEqual(
    canonicalJson(mutated),
    canonicalJson(valid),
    `${name}: PREMISE FAILED — the mutation did not change the input`
  );
  assert.doesNotThrow(
    () => accept(valid),
    `${name}: PREMISE FAILED — the baseline is not accepted`
  );
  if (property) {
    assert.ok(
      property(mutated),
      `${name}: PREMISE FAILED — mutant lacks the property "${propertyName}"`
    );
  }
  assert.throws(() => accept(mutated), `${name}: the mutated input must be REJECT`);
}

test("maxima: the generated objects have the exact required shape", () => {
  const s = maximalBeaconSuffixArtifact();
  assert.equal(s.headers.length, MAX_BEACON_SUFFIX_HEADERS_V1);
  assert.equal(s.headers.length, 2016);

  const i = maximalOrderedSelectedIndicesArtifact();
  assert.equal(i.indices.length, MAX_SELECTED_INDICES_V1);
  assert.equal(i.indices.length, 65536);
  assert.equal(i.indices[0], "0");
  assert.equal(i.indices[i.indices.length - 1], "65535");
});

test("maxima: every digest token in every generated object decodes to exactly 32 bytes", () => {
  const objs = [
    maximalBeaconSuffixArtifact(),
    maximalOrderedSelectedIndicesArtifact(),
    maximalChallengeRecord(),
  ];
  let n = 0;
  for (const o of objs) {
    for (const [k, v] of Object.entries(o)) {
      if (k.endsWith("_digest")) {
        assert.equal(decodeDigestToken(v).length, 32, `${k} must decode to 32 bytes`);
        assert.equal(v.length, 71, `${k} must be a 71-char token`);
        n++;
      }
    }
  }
  assert.equal(n, 9, "expected 9 digest fields across the three artifacts");
});

test("maxima: all generated bytes are canonical round trips", () => {
  for (const o of [
    maximalBeaconSuffixArtifact(),
    maximalOrderedSelectedIndicesArtifact(),
    maximalChallengeRecord(),
  ]) {
    const raw = canonicalJson(o);
    assert.doesNotThrow(() => requireCanonicalBytes(raw), "generated bytes must be canonical");
    assert.equal(canonicalJson(JSON.parse(raw)), raw);
  }
});

